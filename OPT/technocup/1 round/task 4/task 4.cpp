int n;
cin >> n;

vector<vector<vector<int>>> dp(3, vector<vector<int>>(2, vector<int>(n, 0)));

dp[1][0][0] = 1;
dp[2][0][0] = 2;
dp[0][0][0] = 1;
dp[1][1][0] = 0;
dp[2][1][0] = 1;
dp[0][1][0] = 0;

const int mod = 998244353;

for (int i = 1; i < n; i++) {
    dp[1][0][i] =
        (dp[1][0][i - 1] * 2 + dp[2][0][i - 1] * 2 + dp[0][0][i - 1]) % mod;
    dp[2][0][i] =
        (dp[1][0][i - 1] + dp[2][0][i - 1] * 2 + dp[0][0][i - 1] * 2) % mod;
    dp[0][0][i] =
        (dp[1][0][i - 1] * 2 + dp[2][0][i - 1] + dp[0][0][i - 1] * 2) % mod;
    dp[1][1][i] = (dp[2][0][i - 1] + dp[1][1][i - 1] * 2 + dp[2][1][i - 1] * 3 +
                   dp[0][1][i - 1]) %
                  mod;
    dp[2][1][i] = (dp[0][0][i - 1] + dp[1][1][i - 1] + dp[2][1][i - 1] * 2 +
                   dp[0][1][i - 1] * 3) %
                  mod;
    dp[0][1][i] = (dp[1][0][i - 1] + dp[1][1][i - 1] * 3 + dp[2][1][i - 1] +
                   dp[0][1][i - 1] * 2) %
                  mod;
}

cout << (dp[1][1][n - 1] + dp[2][1][n - 1]) % mod << '\n';